            <h5><b>Why is an anchor investor?</b></h5>
            <p>Anchor investors are a part of the Qualified Institutional Buyers (QIB) and aid the price discovery process. Before the book building process is launched, the floor price and the cap price need to be determined. The QIB’s and the anchor investors
                help to fix these prices. In any field especially business, big names and big reputations serve as a boost to investments. In the same way anchor investors serve as an anchor or a support to the IPO. According to SEBI rules and guidelines,
                anchor investors need to stay invested for a period of at least 30 days. This protects retail investors from a sharp fall in prices due to excessive selling by the anchor investors.

            </p>


